package com.model;

public class Veg {
	private int veg_id;
	private String name;
	private String price;
	public int getVeg_id() {
		return veg_id;
	}
	public void setVeg_id(int veg_id) {
		this.veg_id = veg_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
